      <section class="ghazal-notes" id="iqbal-shikwa" aria-labelledby="iqbal-heading">
        <h2 id="iqbal-heading">Iqbal: <cite>Shikwa</cite> &amp; <cite>Jawāb-e-Shikwa</cite></h2>
        <p>
          In <cite>Shikwa</cite>, the complaint runs <strong>from makhlooq to Khāliq</strong> — from
          creation to the Creator. In <cite>Jawāb-e-Shikwa</cite> (the answer), the reply runs
          <strong>from Khāliq to makhlooq</strong> — from the Creator back to creation.
        </p>
      </section>

