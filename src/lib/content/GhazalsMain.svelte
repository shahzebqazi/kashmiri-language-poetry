      <section aria-labelledby="glossary-heading">
        <h2 id="glossary-heading">Core terms</h2>
        <dl class="glossary">
          <dt>Matlaʿ</dt>
          <dd>
            The opening couplet of a ghazal. It sets the rhyme (<span lang="ur-Latn">qāfiya</span>)
            and the refrain (<span lang="ur-Latn">radeef</span>) when those devices are used.
          </dd>

          <dt>Radeef</dt>
          <dd>
            A word or short phrase repeated at the end of both lines of every couplet. It
            follows the rhyme syllables.
          </dd>

          <dt>Kaafiya</dt>
          <dd>
            The rhyming portion immediately before the <span lang="ur-Latn">radeef</span>.
            It must remain consistent across couplets in a classical ghazal.
          </dd>

          <dt>Qaafiya</dt>
          <dd>
            Another term for the rhyme scheme carried by the <span lang="ur-Latn">kaafiya</span>;
            editors sometimes distinguish fine points of rhyme quality, but in ordinary use it
            overlaps with <span lang="ur-Latn">kaafiya</span>.
          </dd>

          <dt>Behr</dt>
          <dd>
            The metrical pattern (prosody) governing syllable length and foot division in a
            line. Urdu meters derive largely from the Arabic–Persian system of
            <span lang="ar-Latn">ʿarūż</span>.
          </dd>

          <dt>Behr-e-Hazaj</dt>
          <dd>
            A common meter built on the foot <span lang="ar" dir="rtl">مَفَاعِیلُنْ</span>
            (<span lang="ar-Latn">mafāʿīlun</span>). In classroom notation the repeated
            pattern is often written as <strong>1–2–2–2</strong> (four feet per
            <span lang="ur-Latn">miṣraʿ</span> in this analysis). The same foot pattern
            applies to <span lang="ur-Latn">miṣraʿ ul-ūlā</span> and
            <span lang="ur-Latn">miṣraʿ uṣ-ṣānī</span> when the line is scanned in four
            segments.
          </dd>

          <dt>Sher</dt>
          <dd>
            A couplet; the ghazal is composed of an uninterrupted sequence of
            independent couplets sharing meter, rhyme, and refrain.
          </dd>

          <dt>Maqtaʿ</dt>
          <dd>
            The closing couplet, where the poet may insert a <span lang="ur-Latn">takhalluṣ</span>
            (pen name).
          </dd>

          <dt>Miṣraʿ ul-ūlā / Miṣraʿ uṣ-ṣānī</dt>
          <dd>
            The first and second half-lines of a couplet: the <span lang="ur-Latn">ūlā</span>
            (“upper”) and <span lang="ur-Latn">ṣānī</span> (“second”) lines. In scansion
            exercises, each is often marked with the same rhythmic formula (e.g.
            1–2–2–2 in Behr-e-Hazaj).
          </dd>

          <dt>ʿArūż (Urooz)</dt>
          <dd>
            The classical system of Arabic–Persian–Urdu prosody; in shorthand notes
            “urooz” refers to the <strong>balance of the couplet</strong>—how the two
            <span lang="ur-Latn">miṣraʿ</span> match in meter and weight.
          </dd>

          <dt>Majāzī</dt>
          <dd>
            Figurative or “worldly” sense: the image as metaphor, story, or human
            situation—before the <span lang="ur-Latn">tanzīl</span> (unveiling) of a
            deeper reading.
          </dd>

          <dt>Ḥaqīqī</dt>
          <dd>
            The “real” or spiritual sense: the inward, unseen dimension (often
            <span lang="ur-Latn">ishq</span> or divine address) that Sufi and classical
            readers hear beneath the <span lang="ur-Latn">majāzī</span> surface.
          </dd>

          <dt>Khāliq</dt>
          <dd>
            The Creator.
          </dd>

          <dt>Makhlooq</dt>
          <dd>
            Creation (creatures, the created world).
          </dd>

          <dt>Tasawwuf</dt>
          <dd>
            Islamic mysticism (Sufism): the path of inward purification, discipline, and
            love-centered knowledge of God. In poetry, Sufi registers often overlap with
            <span lang="ur-Latn">ḥaqīqī</span> meaning, not only
            <span lang="ur-Latn">majāzī</span> romance.
          </dd>
        </dl>
      </section>

      <section class="ghazal-notes" id="tasawwuf" aria-labelledby="tasawwuf-heading">
        <h2 id="tasawwuf-heading">Tasawwuf</h2>
        <p class="lede">
          <strong>Tasawwuf</strong> (Arabic <span lang="ar" dir="rtl">تصوّف</span>) names the
          Sufi tradition: ethical and spiritual practice (often through love, remembrance,
          and guidance) aimed at polishing the heart. In Urdu and Persian ghazal, vocabulary
          of wine, cup, idol, and beloved frequently slides between
          <span lang="ur-Latn">majāzī</span> worldly love and
          <span lang="ur-Latn">ḥaqīqī</span> meaning, a doubleness classical readers are
          trained to hear.
        </p>
      </section>

      <section class="ghazal-notes" id="behr-hazaj" aria-labelledby="behr-hazaj-heading">
        <h2 id="behr-hazaj-heading">Behr-e-Hazaj: pattern</h2>
        <p>
          In one common teaching notation, each line of Behr-e-Hazaj divides into four feet
          summarized as <strong>1–2–2–2</strong> for both the first and second
          <span lang="ur-Latn">miṣraʿ</span>:
        </p>
        <table class="meter-table">
          <caption>Behr-e-Hazaj — rhythmic formula (pedagogical)</caption>
          <thead>
            <tr>
              <th scope="col">Line</th>
              <th scope="col">Foot pattern</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Miṣraʿ ul-ūlā</td>
              <td>1 – 2 – 2 – 2</td>
            </tr>
            <tr>
              <td>Miṣraʿ uṣ-ṣānī</td>
              <td>1 – 2 – 2 – 2</td>
            </tr>
          </tbody>
        </table>
        <p>
          Example (in this meter): “Na jāne kyun huī mujhe ko tasallī hai na jāne kyun” — the
          line is scanned as <span lang="ur-Latn">ūlā</span> in Behr-e-Hazaj with the 1–2–2–2
          weight across the four folds.
        </p>
      </section>

      <section class="ghazal-notes" id="urooz" aria-labelledby="urooz-heading">
        <h2 id="urooz-heading">ʿArūż (urooz): balance of the couplet</h2>
        <p>
          “Urooz” here is shorthand for the prosodic system (<span lang="ar-Latn">ʿarūż</span>)
          that ensures the two halves of a <span lang="ur-Latn">sher</span> agree in meter:
          the <span lang="ur-Latn">ūlā</span> and <span lang="ur-Latn">ṣānī</span> lines carry
          the same <span lang="ur-Latn">behr</span> (here, Behr-e-Hazaj).
        </p>
      </section>

      <section class="ghazal-notes" id="majazi" aria-labelledby="majazi-heading">
        <h2 id="majazi-heading">Majāzī &amp; ḥaqīqī</h2>
        <p>
          <strong>Majāzī</strong>: the “worldly” or figurative layer — the poem as human
          story, image, or wit. <strong>Ḥaqīqī</strong>: the spiritual or “unseen” layer —
          address to the Real, or the inward meaning of the same images.
        </p>
      </section>

      <section class="ghazal-notes" id="khaliq" aria-labelledby="khaliq-heading">
        <h2 id="khaliq-heading">Khāliq &amp; makhlooq</h2>
        <p>
          <strong>Khāliq</strong> is the Creator; <strong>makhlooq</strong> is creation. In
          complaint and praise poems, the voice often moves between these poles (see Iqbal
          below).
        </p>
      </section>

