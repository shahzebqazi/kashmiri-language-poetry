		<section id="having-problem" aria-labelledby="having-problem-heading">
			<h2 id="having-problem-heading">Having a problem with X</h2>
			<p>
				There is no single special verb that always maps English “have a problem with.” Usually the
				construction is nominal plus copula, for example … <span class="script-translit">sith</span> /
				<span class="script-translit">peth</span> / <span class="script-translit">naal</span>
				<span class="script-translit">masla chu</span> (“there is a problem with …”). The postposition
				varies by dialect (<span class="script-translit">sāth</span>,
				<span class="script-translit">peth</span>, <span class="script-translit">nāl</span>, etc.). You may
				also hear <span class="script-translit">mushkil chu</span>,
				<span class="script-translit">pareshāni chu</span>, and similar.
			</p>
		</section>

		<section id="existential" aria-labelledby="existential-heading">
			<h2 id="existential-heading">Existentialism &amp; related philosophical register</h2>
			<p class="meta">
				In academic South Asian discussion this layer is often Urdu / Perso-Arabic alongside English;
				there is not always a separate, standardized native Kashmiri coinage for every term.
			</p>
			<dl class="glossary">
				<dt>Existentialism (school name)</dt>
				<dd
					>Often <span class="script-translit">wujūdiyat</span> <span class="script-rtl" lang="ur" dir="rtl">وجودیت</span>
					in scholarly Urdu; sometimes elaborated with <span class="script-translit">wujūdiyat pasandī</span>-style
					phrasing.</dd
				>

				<dt>Existence / being</dt>
				<dd
					><span class="script-translit">wujūd</span>; <span class="script-translit">mawjūd</span> (“existent”);
					<span class="script-translit">hastī</span> (being — Persian register).</dd
				>

				<dt>Essence vs existence</dt>
				<dd
					><span class="script-translit">zaat</span> for essence / self; contrasts with
					<span class="script-translit">wujūd</span> in Urdu explanations (e.g. existence “precedes” essence in
					Sartre).</dd
				>

				<dt>Individual</dt>
				<dd><span class="script-translit">fard</span>, <span class="script-translit">shakhṣ</span></dd>

				<dt>Freedom</dt>
				<dd><span class="script-translit">azādī</span> / <span class="script-translit">āzādī</span></dd>

				<dt>Choice</dt>
				<dd><span class="script-translit">ikhtiyār</span>, <span class="script-translit">intikhāb</span>, <span class="script-translit">pasand</span></dd>

				<dt>Responsibility</dt>
				<dd><span class="script-translit">zimmedārī</span>, <span class="script-translit">zimmah</span></dd>

				<dt>Meaning / meaningless</dt>
				<dd
					><span class="script-translit">maʿnī</span> / <span class="script-translit">maānī</span>;
					<span class="script-translit">be-maʿnī</span>, <span class="script-translit">bī-mānī</span></dd
				>

				<dt>Anxiety / dread / unease (“angst”)</dt>
				<dd
					><span class="script-translit">pareshāni</span>, <span class="script-translit">fikar</span>,
					<span class="script-translit">bechainī</span>, <span class="script-translit">kurb</span> (more literary Urdu).</dd
				>

				<dt>Despair</dt>
				<dd><span class="script-translit">māyūsī</span>, <span class="script-translit">nā-ummīdī</span></dd>

				<dt>Death</dt>
				<dd><span class="script-translit">maut</span></dd>

				<dt>Alienation</dt>
				<dd
					><span class="script-translit">ajnabiyat</span>; <span class="script-translit">tanhāī</span> (loneliness);
					<span class="script-translit">inhirāf</span> depending on context.</dd
				>

				<dt>Absurd (Camus sense)</dt>
				<dd
					>Often periphrastic (<span class="script-translit">bī-mānī duniyā</span>,
					<span class="script-translit">lā-maʿniyat</span>) or English <em>absurd</em>.</dd
				>
			</dl>
