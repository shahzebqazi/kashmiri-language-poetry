			<h2 id="cautions-heading">Cautions</h2>
			<blockquote>
				<p>
					<strong><span class="script-translit">Wujūd</span></strong> is central in Islamic metaphysics and Sufism
					(e.g. <span class="script-translit">waḥdat al-wujūd</span>) — not the same discursive object as European
					existentialism. Context matters.
				</p>
				<p>
					<strong>Kashmir Shaivism</strong> uses its own technical vocabulary (e.g. Prakāśa, Vimarśa, Cit) — a
					different system from “existentialism” as a modern European label.
				</p>
