		<header>
			<h2>Attention and the concrete world</h2>
		</header>

		<p>
			Object writing is a practice used in creative writing pedagogy in which the writer
			fixes attention on a single object, person, or moment and records sense-bound detail
			before interpretation takes over. The aim is not description for its own sake but the
			disciplined gathering of texture, temperature, sound, weight, and rhythm so that later
			drafts can stay rooted in experience rather than abstraction.
		</p>

		<p>
			In typical exercises, the writer sets a short timer, names a starting focus, and
			writes continuously in the first person. Sight, hearing, touch, smell, and taste are
			rotated deliberately; movement and spatial relation (near and far, above and below) are
			included so the passage gains bodily presence. The rule against summary judgment—no
			thesis, no moral—is what keeps the language close to the world as perceived.
		</p>

		<blockquote>
			The value lies in training precision: verbs that carry motion, nouns that resist
			generalization, and attention to the order in which sensations arrive.
		</blockquote>

		<p>
			For readers of ghazal and other lyric forms, object writing can serve as a companion
			exercise. Couplets often hinge on a concrete image before the turn of thought;
			practicing small, sense-led paragraphs can sharpen the ear for where a line breaks and
			where a metaphor earns its place.
		</p>

		<p>
			Revision usually begins by lifting the strongest images from the draft and discarding
			commentary that crept in early. What remains is not a poem yet but a cluster of true
			perceptions that any formal structure—including the ghazal—can organize without
			smothering.
		</p>
	</article>
